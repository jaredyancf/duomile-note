#颜色填充









import  turtle   as   t
t.speed(0)
t.pensize(5)#画笔粗细
#circle(r,  c,  step)   r半径   c角度   step步数
t.pencolor('blue')  #画笔颜色
t.fillcolor('gold') #填充颜色

t.begin_fill()  #开始填充
t.circle(100,steps=4)
t.end_fill()  #结束填充


t.up()

t.goto(200,0)
t.down()
t.fillcolor('orange') #填充颜色
t.begin_fill()  #开始填充
t.circle(100,steps=8)
t.end_fill()  #结束填充




t.done()









