
#一笔画线
import turtle as t  
#导入画笔库  , turtle是画笔库的名字
#as  t是给画笔库turtle取个别名t
t.bgpic('level03.png') #导入背景图片
t.seth(90)#面向90度方向
t.fd(150) #前进150


t.rt(90)
t.fd(100)



t.rt(90)
t.fd(100)

t.rt(-90)
t.fd(50)

t.done()#为了让界面不闪退



#走迷宫
import  turtle as t
t.speed(0)  #画笔速度
t.bgpic('racetrack.png')
t.pencolor('red')  #画笔颜色
t.pensize(10)  #画笔粗细
t.up() #抬笔
t.goto(400,-220)#   移到(x,y)
t.down()  #落笔
#t.fd(1)  #画笔的默认坐标(0,0),   默认方向朝右, 0度



t.seth(90)
t.fd(500)

t.lt(90)
t.fd(500)
t.lt(90)
t.fd(100)

t.lt(90)
t.fd(300)



t.rt(95)
t.fd(250)

t.lt(35)
t.fd(260)


t.done()






