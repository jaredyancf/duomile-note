#import  导入
#turtle画图库
import  turtle    as  t
#bgpic: 载入背景图片
t.bgpic('level01.png')
#画笔的默认方向是0
t.fd(50)  #  fd(50) 前进50
t.seth(90)  #seth(90)  面向90度方向
t.fd(50) 
t.seth(180)
t.fd(100)

t.seth(270)
t.fd(100) 

t.seth(0)
t.fd(100) 
#done结束
t.done()


